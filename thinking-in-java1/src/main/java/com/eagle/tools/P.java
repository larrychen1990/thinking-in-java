//The P.rint & P.rintln shorthand
package com.eagle.tools;

public class P {
	public static void prt(Object obj) {
		System.out.print(obj);
	}

	public static void prt(String s) {
		System.out.print(s);
	}

	public static void prt(char[] s) {
		System.out.print(s);
	}

	public static void prt(char c) {
		System.out.print(c);
	}

	public static void prt(int i) {
		System.out.print(i);
	}

	public static void prt(long l) {
		System.out.print(l);
	}

	public static void prt(float f) {
		System.out.print(f);
	}

	public static void prt(double d) {
		System.out.print(d);
	}

	public static void prt(boolean b) {
		System.out.print(b);
	}

	public static void prtln() {
		System.out.println();
	}

	public static void prtln(Object obj) {
		System.out.println(obj);
	}

	public static void prtln(String s) {
		System.out.println(s);
	}

	public static void prtln(char[] s) {
		System.out.println(s);
	}

	public static void prtln(char c) {
		System.out.println(c);
	}

	public static void prtln(int i) {
		System.out.println(i);
	}

	public static void prtln(long l) {
		System.out.println(l);
	}

	public static void prtln(float f) {
		System.out.println(f);
	}

	public static void prtln(double d) {
		System.out.println(d);
	}

	public static void prtln(boolean b) {
		System.out.println(b);
	}
} // /:~
